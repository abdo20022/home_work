// Write a Dart program that declares two numeric variables and performs addition, subtraction, multiplication, division, and modulo on them. Print each result.
void main() {
  int num1 = 10;
  int num2 = 10;
  num sum = num1 + num2;
  print(sum);
  num subtraction = num1 - num2;
  print(subtraction);
  num multiplication = num1 * num2;
  print(multiplication);
  num division = num1 / num2;
  print(division);
  num modulo = num1 % num2;
  print(modulo);
}
